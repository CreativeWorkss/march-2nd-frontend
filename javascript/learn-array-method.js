let arr = ["bangalore", "mangalore", "udupi", "dharwad", "shivamogga"];

// push and pop method

arr.push("Hassan");
console.log(arr);
arr.pop();
console.log(arr);
arr.pop();
console.log(arr);

// shift and unshift method

arr.unshift("Hassan");
console.log(arr);

arr.shift();
console.log(arr);

// sort method

arr.sort();
console.log(arr);

// reverse method

arr.reverse();
console.log(arr);
